import React, { useState, useEffect } from 'react';
import { View, Text, Button, StyleSheet } from 'react-native';

const TimersDemo: React.FC = () => {
  const [countdown, setCountdown] = useState<number>(10);
  const [stopwatch, setStopwatch] = useState<number>(0);
  const [isRunning, setIsRunning] = useState<boolean>(false);

  // Countdown Timer
  useEffect(() => {
    let countdownTimer: ReturnType<typeof setInterval>;
    if (countdown > 0) {
      countdownTimer = setInterval(() => {
        setCountdown(prev => prev - 1);
      }, 1000);
    }

    return () => {
      if (countdownTimer) clearInterval(countdownTimer);
    };
  }, [countdown]);

  // Stopwatch Timer
  useEffect(() => {
    let stopwatchTimer: ReturnType<typeof setInterval>;

    if (isRunning) {
      stopwatchTimer = setInterval(() => {
        setStopwatch(prev => prev + 1);
      }, 1000);
    }

    return () => {
      if (stopwatchTimer) clearInterval(stopwatchTimer);
    };
  }, [isRunning]);

  return (
    <View style={styles.container}>
      <Text style={styles.title}>Timer Demo</Text>

      {/* Countdown Timer */}
      <Text style={styles.timerLabel}>Countdown Timer:</Text>
      <Text style={styles.timer}>{countdown}s</Text>

      {/* Stopwatch */}
      <Text style={styles.timerLabel}>Stopwatch:</Text>
      <Text style={styles.timer}>{stopwatch}s</Text>

      {/* Buttons */}
      <View style={styles.buttonContainer}>
        <Button
          title={isRunning ? 'Pause' : 'Start'}
          onPress={() => setIsRunning(prev => !prev)}
        />
        <Button title="Reset Stopwatch" onPress={() => setStopwatch(0)} />
        <Button title="Reset Countdown" onPress={() => setCountdown(10)} />
      </View>
    </View>
  );
};

export default TimersDemo;

const styles = StyleSheet.create({
  container: {
    flex: 1,
    justifyContent: 'center',
    alignItems: 'center',
    backgroundColor: '#fff',
    padding: 20,
  },
  title: {
    fontSize: 24,
    fontWeight: 'bold',
    marginBottom: 20,
  },
  timerLabel: {
    fontSize: 18,
    fontWeight: 'bold',
    marginTop: 20,
  },
  timer: {
    fontSize: 32,
    fontWeight: 'bold',
    marginVertical: 10,
    color: 'blue',
  },
  buttonContainer: {
    marginTop: 20,
    width: '80%',
    gap: 10,
  },
});
